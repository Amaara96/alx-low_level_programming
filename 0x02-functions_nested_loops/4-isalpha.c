/**
  * _isalpha -  checks the code for ALX students
  *
  * Description: The code checks for alphabets only
  *
  * @c: the letter must either be uppercase or lowercase
  *
  * Return:  Always return to 0
*/

int _isalpha(int c)
{
	if (c == 67 || c == 99)
		return (1);
	else
		return (0);
}
